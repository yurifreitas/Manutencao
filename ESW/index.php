<h1>Designer</h1>
<br> O designer é um profissional que desempenha atividade especializada de caráter técnico-científico, criativo e artístico para elaboração de projetos de design passíveis de serialização ou industrialização que atendam, tanto no aspecto de uso quanto no aspecto de percepção, a necessidades materiais e de informação visual

Em inglês, o termo se refere a qualquer indivíduo que esteja ligado a alguma atividade criativa ou de projeto.

Esse anglicismo foi adotado no final do século XX no Brasil com o objetivo de universalizar as profissões ligadas ao projeto. Isso tem ocorrido e a maioria das universidades preferem o termo "designer" pelo fato de o termo "projeto" já existir e ser um sinônimo muito próximo do termo "design".

<br><br>
<h1>Design</h1><br>
Design é a idealização, criação, desenvolvimento, configuração, concepção, elaboração e especificação de artefatos, normalmente produzidos industrialmente ou por meio de sistema de produção seriada que demanda padronização dos componentes e desenho normalizado. Essa é uma atividade estratégica, técnica e criativa, normalmente orientada por uma intenção ou objetivo, ou para a solução de um problema.

Exemplos de coisas que se podem projetar incluem muitos tipos de objetos, como utensílios domésticos, vestimentas, máquinas, ambientes, serviços, marcas e também imagens, como em peças gráficas, famílias de letras (tipografia), livros e interfaces digitais de softwares ou de páginas da Internet, entre outros.

O design recorre a algumas disciplinas como por exemplo a antropometria, a economia, a biônica e a ecologia, entre outras.

O design é também uma profissão, cujo profissional é o designer. Os designers normalmente se especializam em uma determinada area ou atividade. Atualmente as especializações mais comuns são o design de produto, design visual, design de moda, design de interiores e o design gráfico.




